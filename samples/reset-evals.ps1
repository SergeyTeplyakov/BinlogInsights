<#
.SYNOPSIS
    Resets evaluation directories from templates.

.DESCRIPTION
    For each template, creates two eval variants:
      - <name>_plain     : no tool instructions (baseline)
      - <name>_with_tool : includes .github/copilot-instructions.md for binlog-insights
    Each variant gets a fresh binlog generated by building the project.
    Build artifacts (bin/obj) are cleaned to keep the workspace minimal.

.EXAMPLE
    .\reset-evals.ps1

.EXAMPLE
    # Reset only one scenario
    .\reset-evals.ps1 -Template MissingType
#>
[CmdletBinding()]
param(
    [string]$Template = ''
)

$ErrorActionPreference = 'Stop'
$samplesDir = $PSScriptRoot
$templatesDir = Join-Path $samplesDir 'templates'
$evalsDir = Join-Path $samplesDir 'evals'
$repoRoot = Split-Path $samplesDir -Parent
$instructionsSource = Join-Path $repoRoot '.github' 'copilot-instructions.md'

function Copy-Template {
    param([string]$SourceDir, [string]$DestDir)

    New-Item -ItemType Directory -Path $DestDir -Force | Out-Null
    # Copy source files only (skip bin/obj/binlog)
    Get-ChildItem $SourceDir -File |
        Where-Object { $_.Extension -notin '.binlog' } |
        Copy-Item -Destination $DestDir
    # Copy subdirectories (but not bin/obj)
    Get-ChildItem $SourceDir -Directory |
        Where-Object { $_.Name -notin 'bin', 'obj' } |
        ForEach-Object { Copy-Item -Recurse $_.FullName (Join-Path $DestDir $_.Name) }
}

function Build-Binlog {
    param([string]$ProjectDir, [string]$Name)

    $csproj = Get-ChildItem $ProjectDir -Filter '*.csproj' | Select-Object -First 1
    if (-not $csproj) { return }

    $binlog = Join-Path $ProjectDir "$Name.binlog"
    # Restore first (may fail for RestoreFail — that's expected)
    dotnet restore $csproj.FullName 2>$null
    dotnet build $csproj.FullName "/bl:$binlog" 2>$null
    # Clean build outputs to keep eval dir minimal
    Remove-Item -Recurse -Force (Join-Path $ProjectDir 'bin') -ErrorAction SilentlyContinue
    Remove-Item -Recurse -Force (Join-Path $ProjectDir 'obj') -ErrorAction SilentlyContinue
    # The nonexistent/ dir may be created by BadProps OutputPath override
    $nonexistent = Join-Path $ProjectDir '..' '..' 'nonexistent'
    Remove-Item -Recurse -Force $nonexistent -ErrorAction SilentlyContinue

    if (Test-Path $binlog) { Write-Host "    binlog: OK" -ForegroundColor Green }
    else { Write-Host "    binlog: MISSING" -ForegroundColor Red }
}

function Reset-Sample {
    param([string]$TemplateName)

    $src = Join-Path $templatesDir $TemplateName

    foreach ($variant in @('plain', 'with_tool')) {
        $destName = "${TemplateName}_${variant}"
        $dest = Join-Path $evalsDir $destName

        Write-Host "  $destName" -ForegroundColor Yellow

        # Wipe and re-copy
        if (Test-Path $dest) { Remove-Item -Recurse -Force $dest }
        Copy-Template $src $dest

        # Remove scenario.md from eval dirs — it's for our reference, not Copilot's
        Remove-Item (Join-Path $dest 'scenario.md') -ErrorAction SilentlyContinue

        # Generate binlog
        Build-Binlog $dest $TemplateName

        # Add copilot instructions only for with_tool variant
        if ($variant -eq 'with_tool') {
            $ghDir = Join-Path $dest '.github'
            New-Item -ItemType Directory -Path $ghDir -Force | Out-Null
            if (Test-Path $instructionsSource) {
                Copy-Item $instructionsSource (Join-Path $ghDir 'copilot-instructions.md')
                Write-Host "    copilot-instructions: added" -ForegroundColor Cyan
            }
            else {
                Write-Host "    copilot-instructions: NOT FOUND at $instructionsSource" -ForegroundColor Red
            }
        }
    }
}

# --- Main ---

if (-not (Test-Path $templatesDir)) {
    Write-Error "Templates directory not found: $templatesDir"
    exit 1
}

$templates = if ($Template) {
    @(Get-Item (Join-Path $templatesDir $Template))
}
else {
    Get-ChildItem $templatesDir -Directory
}

if ($templates.Count -eq 0) {
    Write-Error "No templates found in $templatesDir"
    exit 1
}

New-Item -ItemType Directory -Path $evalsDir -Force | Out-Null

Write-Host "=== Resetting evaluations ===" -ForegroundColor Cyan
Write-Host ""

foreach ($t in $templates) {
    Write-Host "$($t.Name):" -ForegroundColor Cyan
    Reset-Sample $t.Name
    Write-Host ""
}

Write-Host "Done. Eval directories are in: $evalsDir" -ForegroundColor Green
Write-Host ""
Write-Host "How to run an evaluation:" -ForegroundColor Gray
Write-Host "  1. Open an eval folder as a VS Code workspace (File > Open Folder)" -ForegroundColor Gray
Write-Host "  2. Open Copilot Chat in agent mode" -ForegroundColor Gray
Write-Host "  3. Paste the prompt from samples/prompt.md" -ForegroundColor Gray
Write-Host "  4. Observe the outcome and fill in the scenario.md checklist" -ForegroundColor Gray
